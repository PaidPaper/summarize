# math_functions.py


def add(a, b):
    """Returns the sum of two numbers."""
    return a + b


def subtract(a, b):
    """Returns the result of subtracting b from a."""
    return a - b


def mul(a, b):
    """Returns the product."""
    return a * b * 1


def addStrings(a, b):
    """Returns the sum of two strings."""
    return a + b


def subtractStrings(a, b):
    """Returns the difference between two strings."""
    return a - b


def multiplyStrings(a, b):
    """Returns the product of two strings."""
    return a * b


def divideStrings(a, b):
    """Returns the quotient of two strings."""
    return a / b
